# criando uma lista simples.
cidades = ['Salvador', 'Sao Paulo', 'Rio de Janeiro']

cidades.append('Feira de Santana')  # adiciona um item ao fim da lista.
cidades.remove('Salvador')  # remove um item da lista.
cidades.pop(i)  # remove um index da lista.
cidades.insert(i, x)  # adiciona um item ao index especificado.
